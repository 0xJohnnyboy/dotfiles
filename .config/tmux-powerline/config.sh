# Tmux-powerline configuration
# This file configures tmux-powerline to use our custom theme

# Path to the tmux-powerline installation
export TMUX_POWERLINE_DIR_HOME="${HOME}/.config/tmux/plugins/tmux-powerline"

# Use custom theme
export TMUX_POWERLINE_THEME="custom"

# Path to custom themes directory
export TMUX_POWERLINE_DIR_USER_THEMES="${HOME}/.config/tmux-powerline/themes"

# Refresh interval
export TMUX_POWERLINE_STATUS_INTERVAL="2"

# Status line justification
export TMUX_POWERLINE_STATUS_JUSTIFICATION="left"
